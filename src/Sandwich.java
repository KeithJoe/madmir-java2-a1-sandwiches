
public class Sandwich {
	//Declares variables
	static String mainIngredient;
	static String breadType;
	static double price;
	
	//returns all the declarations
	public String getMainIngredient() {
		return mainIngredient;
	}
	public String getBreadType() {
		return breadType;
	}
	public double getPrice() {
		return price;
	}
	public void setMainIngredient(String newMainIngredient) {
		mainIngredient = newMainIngredient;
	}
	public void setBreadType(String newBreadType) {
		breadType = newBreadType;
	}
	public void setPrice(double newPrice) {
		price = newPrice;
	}
}


	



